x = 23
y = 12.3
z = 12j # we use 'j' for representing complex number
print(type(x));
print(type(y));
print(type(z));

# Integer
a = 1227348742474
b = -12234353546
c = 0
print(type(a))
print(type(b))
print(type(c))

# Float
e = 122734.8742474
f = -12234.353546
g = 0.2032
print(type(e))
print(type(f))
print(type(g))

# Complex
i = 12 + 3j
j = -12 - 0.4j
k = 4j
print(type(i))
print(type(j))
print(type(k))